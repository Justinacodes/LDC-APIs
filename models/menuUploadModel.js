const mongoose = require("mongoose");

const menuSchema = new mongoose.Schema({
  foodName: {
    type: String,
    required: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  image: {
    type: String,
    required: true,
  },
  createdAt: {
    type: Date,
    default: Date.now,
  },
});
const Menu = mongoose.model("Menu", menuSchema);
module.exports = Menu;

